package example;

public class evenodd {
	 public static void evenodd(int a) {
	
	        if (a%2==0) {
	            System.out.println(a + " is even.");
	        }
	        else {
	            System.out.println(a + " is odd.");
	        }
	        }


	    public static void main(String[] args) {
	        evenodd(4);
	        evenodd(7);// Change this number to test different values

	       

}
}

